def add_two(a, b):
    return a + b

def subtract_two(a, b):
    if a >= b:
        return a - b
    else:
        return b - a

def multiple_two(a, b):
    return a * b

def division_two(a, b):
    return a // b

print(add_two(34,56))
print(subtract_two(54, 90))
print(multiple_two(4,5))
print(division_two(100,50))
